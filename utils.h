int i2v(int index) {
    if (index < 3) return index;
    return 3 * (1 << (index - 3));
}

int v2i(int value) {
    if(value % 3 != 0) return value;
    value = value / 3;
    int ans = 0;
    while((1 << ans) < value) ans++;
    return ans + 3;
}


